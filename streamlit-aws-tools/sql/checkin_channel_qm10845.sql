SELECT DISTINCT
    t200.PNR_REF,
    t845.TRAVLR_UCI,
    t845.TRAVLR_UPI,
    t240.AAIRALPC,
    t240.PRMEFLTN,
    t240.LOCLDEPD,
    t240.DEPUPOR,
    t240.ARVLPOR,
    t845.PAX_CKI_LOC_DTE AS UTC_CHK_IN_DATE,
    t845.PAX_CKI_LOC_TIM AS UTC_CHK_IN_TIME,
    t845.CKI_CARR_SET_ADDR AS CHK_IN_WORKSTATION,
    t845.ACCEPT_CHANNEL_CODE AS CHK_IN_CHANNEL,
    t845.USER_ID AS CHK_IN_USER_ID
FROM PQMF.QHB0240_FLIGHT_LEG_DEPART t240
JOIN PQMF.QHB0241_TRAVLR_FLIGHT_LEG t241
  ON t240.FMFL_ID = t241.FMFL_ID
 AND t240.FL_VERSION_NO = t241.FL_VERSION_NO
JOIN PQMF.QHB0221_FLIGHT_ITINERARY t221
  ON t241.TRAVLR_UPI = t221.TRAVLR_UPI
 AND t241.TRAVLR_UCI = t221.TRAVLR_UCI
JOIN PQMF.QHBS200_TRAVELLER t200
  ON t221.PNR_REF = t200.PNR_REF
 AND t221.TRAVLR_UCI = t200.TRAVLR_UCI
JOIN PQMF.QM10845_HISTDCDOM t845
  ON t845.FMFL_ID = t241.FMFL_ID
 AND t845.FL_VERSION_NO = t241.FL_VERSION_NO
 AND t845.TRAVLR_UCI = t241.TRAVLR_UCI
 AND t845.TRAVLR_UPI = t241.TRAVLR_UPI
WHERE
    (t200.PNR_REF  = NULLIF(TRIM(?), '') OR NULLIF(TRIM(?), '') IS NULL)
AND (t240.PRMEFLTN = NULLIF(TRIM(?), '') OR NULLIF(TRIM(?), '') IS NULL)
AND (t240.DEPUPOR  = NULLIF(TRIM(?), '') OR NULLIF(TRIM(?), '') IS NULL)
AND (t240.LOCLDEPD >= ? OR ? IS NULL)
AND (t240.LOCLDEPD <= ? OR ? IS NULL)
ORDER BY t240.LOCLDEPD DESC;