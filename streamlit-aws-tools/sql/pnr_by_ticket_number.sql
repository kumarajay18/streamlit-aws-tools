SELECT DISTINCT
    t200.PNR_REF,
    t200.TRAVLR_UCI,
    t200.TRAVLR_FIRST_NAME,
    t200.TRAVLR_LAST_NAME,
    t223.TKT_NUMBER,
    t223.TKT_TYPE_CODE,
    t223.CONJUNCTION_TKT_NUMBER
FROM PQMF.QHB0223_TICKET t223
JOIN PQMF.QHBS200_TRAVELLER t200
  ON t223.TRAVLR_UCI = t200.TRAVLR_UCI
WHERE
    (t200.PNR_REF = NULLIF(TRIM(?), '') OR NULLIF(TRIM(?), '') IS NULL)
AND (t223.TKT_NUMBER = NULLIF(TRIM(?), '') OR NULLIF(TRIM(?), '') IS NULL)
ORDER BY t223.TKT_NUMBER;